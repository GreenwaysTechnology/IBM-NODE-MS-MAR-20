//booleans
let isActive = true;
console.log(`Is Active ${isActive}`);

//decision
if(isActive){
    console.log(`It is active`)
}else{
    console.log(`It is not active`)
}
//an operator tenary operator
isActive ? console.log(`It is active`) :   console.log(`It is not active`)

